export const getDistrictSummaries = () => {
  const districts = ['Srinagar', 'Ganderbal', 'Bandipora', 'Budgam', 'Baramulla', 'Anantnag', 'Jammu', 'Leh'];
  
  return districts.map(district => {
    const bodies = algalBloomData.filter(d => d.district.includes(district));
    const activeBlooms = bodies.filter(d => d.riskLevel === 'Critical' || d.riskLevel === 'High').length;
    
    let avgSeverity: RiskLevel = 'Low';
    if (activeBlooms >= 3) avgSeverity = 'Critical';
    else if (activeBlooms >= 1) avgSeverity = 'High';
    else if (bodies.some(b => b.riskLevel === 'Moderate')) avgSeverity = 'Moderate';
    
    let trend: 'worsening' | 'stable' | 'improving' = 'stable';
    if (district === 'Srinagar') trend = 'worsening'; // Dal, Nigeen, Anchar, Khushalsar, Gilsar, Brari Nambal
    if (district === 'Ganderbal') trend = 'stable'; // Seasonal/mixed
    if (district === 'Bandipora') trend = 'stable'; // Restoration-sensitive
    if (district === 'Budgam') trend = 'stable';
    
    return {
      district,
      waterbodies: bodies.length,
      activeBlooms,
      avgSeverity,
      trend,
      notes: bodies.map(b => b.waterbodyName).join(', ')
    };
  });
};
