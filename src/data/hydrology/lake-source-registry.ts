/**
 * Look up a source by ID
 */
export function getSource(sourceId: string): LakeSource | undefined {
  return lakeSourceRegistry.find(s => s.sourceId === sourceId);
}

/**
 * Get all sources by type
 */
export function getSourcesByType(type: LakeSource['sourceType']): LakeSource[] {
  return lakeSourceRegistry.filter(s => s.sourceType === type);
}

/**
 * Get all high-confidence sources
 */
export function getHighConfidenceSources(): LakeSource[] {
  return lakeSourceRegistry.filter(s => s.confidence === 'High');
}

