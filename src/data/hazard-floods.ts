// ─── Helper functions ───────────────────────────────────────────────────────

export function getRiskVariant(level: RiskLevel): string {
  switch (level) {
    case 'Critical': return 'critical';
    case 'High': return 'danger';
    case 'Moderate': return 'warning';
    case 'Low': return 'success';
  }
}

export function getStatusColor(status: CurrentStatus): string {
  switch (status) {
    case 'Active Alert': return 'text-red-400 bg-red-500/10 border-red-500/30';
    case 'Watch': return 'text-amber-400 bg-amber-500/10 border-amber-500/30';
    case 'Normal': return 'text-emerald-400 bg-emerald-500/10 border-emerald-500/30';
    case 'Historical': return 'text-slate-400 bg-slate-500/10 border-slate-500/30';
  }
}

export function getVerificationChip(status: VerificationStatus): string {
  switch (status) {
    case 'Verified': return 'text-emerald-400 bg-emerald-500/10 border-emerald-500/30';
    case 'Modelled': return 'text-amber-400 bg-amber-500/10 border-amber-500/30';
    case 'Satellite-Derived': return 'text-cyan-400 bg-cyan-500/10 border-cyan-500/30';
  }
}

export function getFloodTypeColor(type: FloodType): string {
  switch (type) {
    case 'Riverine': return 'text-blue-400 bg-blue-500/10 border-blue-500/30';
    case 'Urban': return 'text-purple-400 bg-purple-500/10 border-purple-500/30';
    case 'Floodplain': return 'text-teal-400 bg-teal-500/10 border-teal-500/30';
    case 'Flash': return 'text-orange-400 bg-orange-500/10 border-orange-500/30';
  }
}
